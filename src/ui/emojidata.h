inline QVector<EmojiEntry> emojiMatching(const QString &prefix)
{
    QVector<EmojiEntry> result;
    for (const auto &e : emojiTable())
        if (e.shortcode.startsWith(prefix, Qt::CaseInsensitive))
            result.append(e);
    return result;
}
